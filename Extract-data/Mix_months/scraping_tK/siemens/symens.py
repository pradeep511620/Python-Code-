for li in l:
    print(li)
    browser.get(li)
    soup=BeautifulSoup(browser.page_source,'html.parser')
    product=soup.find('span',class_='productIdentifier').text
    print(product)
    try:
        image=soup.find('div',class_='pictureArea').find('img').get('src')
        print(image)
    except:
        pass
    description=soup.find('div',class_='productdescription').text
    print(description.strip())
    table=soup.find('table',class_='ProductDetailsTable').find_all('td')
    Breadcrumb=soup.find('div',class_='Breadcrumb').find_all('a',class_='breadcumbInternalLink')
    Bread=[]
    for b in Breadcrumb:
        Bread.append(b.text)
    print(Bread)

    y=0
    tr1=[]
    td1=[]
    for tr in  table:
        if "Product"==tr.text or "Price data"==tr.text or "Delivery information"==tr.text or "Additional Product Information"==tr.text:
            continue
        if  "Classifications"==tr.text or "REACH Art. 33 " in tr.text:
            break
        y+=1
        if y%2==0:

            tr1.append(tr.text.strip())
        else:
            td1.append(tr.text.strip())

    print(td1)
    print(tr1)
    for j,k in zip(td1, tr1):
        print(j, " : ",  k.replace('\n',''))
        save_details: TextIO = open("siemens.txt", "a+", encoding="utf-8")
        save_details.write(
            "\n" + li + "\t" + product + "\t" + '->'.join(Bread) + "\t" + image + "\t" + description.strip() + "\t" + j + "\t" +"rp_"+k.replace('\n\n','|'))
        print("End")
        save_details.close()
